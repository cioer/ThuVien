    private String getStr(JTextField tf) {
        String rs = tf.getText().trim();
        if (rs.equals("")) {
            return null;
        } else {
            return rs;
        }
    }

    private String getStr(JComboBox cmb) {
        String rs = cmb.getSelectedItem().toString();
        if (rs.equals("")) {
            return null;
        } else {
            return rs;
        }
    }

